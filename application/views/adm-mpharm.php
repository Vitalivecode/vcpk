                                                        <h4>Schedule of Admissions:</h4>
                                                        <p class="text-justify">Usually the admissions by the TSPGECET Convenor start in July/Aug. and close by Sep/Oct every year. However the schedule may vary from year to year. After the counseling, the left over seats shall be filled by the colleges through spot admission according to the rules prescribed by the State Government</p>
                                                        
