                                                        <h4>Pharm.D (PB) (MORE INFORMATION)</h4>
                                                        <h5>Pharm.D. (Post Baccalaureate):</h5>
                                                        <p class="text-justify">The duration of the course shall be for three academic years (two years of study and one year internship or residency). Pharm. D. (Post Baccalaureate) lateral entry program is a post graduate professional doctoral degree. It was introduced for B Pharm candidates, who want to persue Pharm D (Post Baccalaureate) program, with an aim to to improve clinical and hospital Pharmacy services in India.</p>
                                                        <p class="text-justify">It is a phase of training wherein a student is exposed to actual pharmacy practice or clinical pharmacy services, and acquires skill under supervision so that he or she may become capable of functioning independently.</p>
                                                        
                                                        <h4>Minimum qualification for admission to M.Pharmacy & Pharm.D. (Post Baccalaureate):</h4>
                                                        <p class="text-justify">A pass in B.Pharm from an institution approved by the Pharmacy Council of India under section 12 of the Pharmacy Act.</p>
                                                        <h4>RESERVATION POLICY:</h4>
                                                        <p class="text-justify">Provided that there shall be reservation of seats for the students belonging to the Scheduled Castes, Scheduled Tribes and other Backward Classes in accordance with the instructions issued by the Central Government/State Government/Union Territory Administration as the case may be from time to time.</p>

