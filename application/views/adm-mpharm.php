                                                        <h3>M.Pharmacy & Pharm.D. (Post Baccalaureate)</h3>
                                                        <h4>GPAT / TSPGECET/ Counseling Code: VGSP1</h4>
                                                        <h4>Admission Procedure:</h4> 
                                                        <p class="text-justify">For admission to the professional course like M.Pharmacy & Pharm.D (PB), the Central & State Government conducts entrance exams called GPAT & TSPGECET every year. The students are given ranking based on the marks obtained in the test. Admissions are conducted by the Convener TSPGECET in a systematic manner by means of web counseling, based on the rank obtained in the TSPGECET. Thus opportunity is given to the student to choose the course and college. 70% of seats are filled through the TSPGECET/GPAT rank by the Convener and 30% seats are filled by the management, based on the merit at B.Pharmacy or as per the State government prescribed rules from time to time.
